    /**
     * Display the specified resource.
     *
     * @param  \App\Category  $category
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $cat = Category::all();
        $data = Category::find($id);
        return view('admin.page.category',['cat' => $cat,'data' => $data]);
    }

